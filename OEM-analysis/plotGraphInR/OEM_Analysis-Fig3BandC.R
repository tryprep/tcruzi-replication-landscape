#              Kruskal-Wallis) based on data normality and homogeneity.
#
# Required Input Files:
#   1. OEM bedGraph: "OEM.bedgraph" containing genome-wide OEM scores.
#   2. BED Files: Genomic coordinates for regions of interest 
#      (e.g., "control.bed", "origins.bed", "MiniInitiationZones.bed").
