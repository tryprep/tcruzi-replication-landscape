# STEP 9: Statistical Analysis (Normality, Homogeneity, ANOVA/Kruskal-Wallis)
# Calculate the mean score per region for the boxplot analysis
region_means <- df_raw[, .(region_mean = mean(score, na.rm = TRUE)), by = .(region_id, type)]

# Safe Shapiro-Wilk (avoids errors when sample size is out of bounds)
safe_shapiro <- function(x) {
  res <- tryCatch({
    if(length(x) >= 3 && length(x) <= 5000) shapiro.test(x)$p.value else NA_real_
  }, error = function(e) {
    message(paste("Shapiro-Wilk error:", e$message))
    NA_real_
  })
  return(as.numeric(res))
}

# Apply normality test by group
shapiro_results <- region_means[, .(shapiro_p = safe_shapiro(region_mean)), by = type]
message("Shapiro-Wilk P-values per group: ", paste(shapiro_results$shapiro_p, collapse = ", "))

# Levene test for homogeneity of variances
levene_result <- leveneTest(region_mean ~ type, data = region_means)
homogeneity <- levene_result$`Pr(>F)`[1] > 0.05
message("Levene Test P-value for homogeneity: ", levene_result$`Pr(>F)`[1])

# Decide which statistical test to use
normality <- all(shapiro_results$shapiro_p > 0.05, na.rm = TRUE)

if(normality && homogeneity) {
  test_used <- "ANOVA"
  aov_res <- aov(region_mean ~ type, data = region_means)
  global_p <- summary(aov_res)[[1]]$`Pr(>F)`[1]
  pairwise_method <- "t.test"
  message("Using Parametric Test: ANOVA and T-tests (Normality and Homogeneity met).")
} else {
  test_used <- "Kruskal-Wallis"
  kruskal_res <- kruskal.test(region_mean ~ type, data = region_means)
  global_p <- kruskal_res$p.value
  pairwise_method <- "wilcox.test"
  message("Using Non-Parametric Test: Kruskal-Wallis and Wilcoxon tests (Normality or Homogeneity failed).")
}
message(paste(test_used, "global P-value:", global_p))

