# STEP 4: Define Processing Function
# Function to process each BED file, extract profiles, and calculate raw bin scores
process_bed_file <- function(bed_file, bg.gr, nbins, region_size) {
  message("Processing ", bed_file)
  
  # 1. Load and prepare BED file (assuming at least chr, start, end)
  bed <- fread(bed_file, header = FALSE)
  setnames(bed, 1:3, c("chr", "start", "end")) 
  
  # Convert to GRanges (start + 1 for 1-based indexing)
  gr <- GRanges(seqnames = bed$chr, ranges = IRanges(start = bed$start + 1, end = bed$end))
  
  # Center regions and tile into bins
  gr.centered <- resize(gr, width = region_size, fix = "center")
  gr.bins <- unlist(tile(gr.centered, n = nbins))
  
  # 2. Find overlaps with bedGraph
  ov <- findOverlaps(gr.bins, bg.gr)
  
  # 3. Aggregate scores per bin
  hits_dt <- data.table(
    queryHits = queryHits(ov), 
    subjectHits = subjectHits(ov)
  )
  
  # Calculate bin and region IDs for each hit
  hits_dt[, bin_id := ((queryHits - 1) %% nbins) + 1]
  hits_dt[, region_id := ((queryHits - 1) %/% nbins) + 1]
  hits_dt[, val := bg.gr$score[subjectHits]]
  
  # Calculate mean score for bins that overlap with the bedgraph
  means <- hits_dt[, .(score = mean(val, na.rm = TRUE)), by = .(region_id, bin_id)]
  
  # 4. Create raw data table for all bins (including those with NA/no overlap)
  all_bins_dt <- data.table(
    region_id = rep(1:length(gr), each = nbins),
    bin_id = rep(1:nbins, length(gr))
  )
  
  # Merge calculated means; non-matching bins will have NA score
  bins_dt <- merge(all_bins_dt, means, by = c("region_id", "bin_id"), all.x = TRUE)
  
  # 5. Calculate Metaprofile (mean across all regions for each bin)
  prof <- bins_dt[, .(mean_signal = mean(score, na.rm = TRUE)), by = bin_id]
  
  # 6. Set type/sample label based on filename
  file_type <- tools::file_path_sans_ext(basename(bed_file))
  prof$type <- file_type
  bins_dt$type <- file_type
  
  # Replace NaN generated by mean(empty set) with 0 for robustness
  prof[, mean_signal := fifelse(is.nan(mean_signal), 0, mean_signal)]
  bins_dt[, score := fifelse(is.nan(score), 0, score)]
  
  return(list(profile = prof, raw = bins_dt))
}

